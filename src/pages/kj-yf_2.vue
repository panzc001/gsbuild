                const scrollTop = $(this).scrollTop();
                const scrop_1 = $('.pho_1').offset().top;
                const scrop_2 = $('.pho_2').offset().top;
                const scrop_3 = $('.pho_3').offset().top;
                const scrop_4 = $('.pho_4').offset().top;
                const scrop_5 = $('.pho_5').offset().top;
